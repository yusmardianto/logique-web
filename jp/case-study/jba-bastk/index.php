      <section class='cs-section client-info'>
        <h2 class="section-title">Background</h2>
        <p class="mb-30px">
          オークション業界では、車両をオークション会場に送る過程で有効なデータを正確に記録できるかどうかが普遍的な問題となっています。というのも、納品された商品は、ある段階以降は移動する必要があり、それぞれの段階において、特に車両が通過する際に発生するさまざまな変化を記録することが難しいためです。また、部品の紛失などもよくあることで、紙とペンというアナログな記録作業は困難を極めます。
        </p>
        <p>
          しかしその解決方法は、簡単なようでいて、そう単純ではありません。JBA IndonesiaとLOGIQUE DIGITAL INDONESIAは、配送の各段階での状況を可視化できる効率的な記録・追跡アプリケーションを開発し、プロセスのデジタル化を図りました。こうすることで、車両全般におけるデータを、各関係者が体系的かつリアルタイムに把握することができるようになったのです。
        </p>
      </section>


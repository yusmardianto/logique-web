              <p class="cs-subtitle-top">Background</p>
              <p class="cs-desc">
              AIAは、成長著しい大手保険会社の一つとして、顧客満足度の継続的な向上を目指しており、その一環として、保険契約の解約手続きをマニュアルからデジタルに移行することを検討しました。現在、テクノロジーの進化は著しく、誰もがあらゆることをオンラインベースのプラットフォームで行いたいと考えるようになっています。AIAはこのニーズに応えるため、このサイトを開発することにしました。
              <br><br>
              AIA Surrenderマイクロサイトを通じて、保険契約の解約手続きをオンラインで行うことができるー。それは、保険契約者が会社を訪問したり連絡したりすることなく、どこにいても簡単に手続きができるということです。さらに、マイクロサイトではリテンション・ツールも開発し、お客さまが解約の意思を維持・軽減できるようにも努めています。
              <br><br>
              その上で、PT. AIA Financialは、LOGIQUE DIGITAL INDONESIAと緊密に連携し、Webサイトをベースにした最適な解約システムの開発をさらに進化させています。このシステムにあらゆる世代がアクセスできるよう、ユーザーフレンドリーでわかりやすいUI/UXデザインによって、さらなるサポートがなされる必要があると考えました。そこで、今回のプロジェクト「AIA Surrender」を立ち上げ、保険契約の解約プロセスを効果的にデジタル化することを目指したのです。

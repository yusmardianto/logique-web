              AIA Surrenderマイクロサイトを作成するために、PHPプログラミング言語を使用するLaravelフレームワークの技術が使用されています。Laravelフレームワークが選ばれた理由は、MVC構造のサポート、パフォーマンスの高さ、良好なセキュリティーなどの利点があるためです。Laravelのモデルコンセプトは、高いチームコラボレーションとしても知られており、その1つが「migrate機能/コンポーネント」です。migrateコンポーネントを使えば、チームはLaravelコンポーザーから直接データベースを設計してMySQLクエリを実行できるので、phpmyadminを使って手動でTableを作成したり、MySQLデータベースファイルを共有する必要がありません。
              <br><br>
              なおUIには、Bootstrapフレームワークを使用したHTMLとCSSの技術が使用されています。Bootstrapは、レスポンシブWebサイト開発で最も人気のあるCSSフレームワークです。これにより、AIA Surrenderマイクロサイトは、PC、タブレット、スマートフォンなど、あらゆるデバイスで見栄えがよいものになります。

              BUMAは、約12,000人の従業員に支えられ、表土の除去、採炭、石炭の輸送、埋め立てから土地修復整地プロセスまで、幅広いサービスを実行できます。 BUMAは、コマツ、キャタピラー、日立、ボルボ、スカニア、メルセデスなどのブランドの2,500台を超える重機を保有、管理しています。
              <br><br>
              可能な限り最高のサービスを提供するために、BUMAはすべての分野でオペレーショナルエクセレンスを目指して努力を続けています。

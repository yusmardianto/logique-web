              <p class="cs-subtitle-top">BUMAについて</p>
              <p class="cs-desc">
              PT Bukit Makmur Mandiri Utama（BUMA）は、1998年に設立された、インドネシアで2番目に大きな独立したマイニング会社です。現在、BUMAは、両方の大規模な採炭などのサービスの提供を通じて、インドネシア内で大きな市場シェアを保持しています。特に石炭産業内において強い競争力を保有しています。

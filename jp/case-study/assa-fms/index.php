              <br><br>
              これは、運輸業界、特に多数の車両を担当するASSAにとって重要な課題です。この課題は、手動の車両管理プロセスがもはや不可能であるという事実にあります。タイプ、色、ドライバー、配達日、けん引日などの車両データを手動で入力してスプレッドシートドキュメントを使用して車両リストを管理すると、実際にかなりの時間がかかり、さらに人為的ミスの可能性が高くなります。これは、非常に複雑な車両引き渡し業務、修理メンテ業務と関連していて、車両の歩留まり、車両引き渡し時の連絡ミスなどを生んでいました。

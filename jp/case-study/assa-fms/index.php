              <p class="cs-subtitle-top">適用技術</p>
              <p class="cs-desc custom4">
              ASSA社向けフリート管理システム（FMS）Webアプリケーションの開発では、さまざまなテクノロジーが使用されました。 Laravelフレームワークなど、それらのいくつかは、MVC構造のサポート、スケーラビリティ、優れたセキュリティ、およびより高パフォーマンスなどの利点のために選択されました。 Lumenは、高速な応答時間を提供することを目的としたAPI開発のマイクロフレームワークとしても使用されました。フロントエンドに関しては、ブートストラップフレームワークを使用して、Webサイトアプリケーションがインタラクティブでレスポンシブな外観となり、さまざまなデバイスで適切に機能できるようにしました。
              <br><br>
              このシステムの開発において、ASSAは車両の引き渡しプロセスをデジタル化しようとしました。そのため、BASTK機能を実行するためのモバイルアプリケーションも開発しました。このアプリケーションはKotlinを使用して開発されたため、Androidプラットフォームで適切に実行できます。
              <br><br>
              さらに、データベースサーバー層には、MySQLを利用しました。次に、上記のすべてのテクノロジーが、信頼性の高いデータ処理機能を備えたAlibabaCloudと組み合わされました。さらに、Alibaba Cloudは、より高いレベルのセキュリティを提供するのに役立つAnti-DDoSテクノロジーを提供しています。

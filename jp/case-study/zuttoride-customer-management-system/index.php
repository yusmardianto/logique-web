              <h2 class="cs-subtitle">Technology Applied</h2>
              <p class="cs-desc">
                ZuttoRide Indonesiaが期待するWebシステム構築に着手する以前に、LOGIQUE DIGITAL INDONESIAはまず、使用するシステムを検討しました。その結果、ZuttoRide Indonesiaが求めるWebサイトには、高いカスタマイズ性と拡張性が必要であることを理解しました。そこでLOGIQUE DIGITAL INDONESIAは、システムの基盤としてCakePHPというフレームワークを用いたWebシステム開発技術を推奨しました。
                <br><br>
                Cake PHP自体は、一般的にアプリケーションやWebサイトの開発者が使用されるPHPフレームワークで、シンプルなものから複雑なレベルのものまでさまざまなものがあります。またこのフレームワークは、最新のPHP、すなわちデータベースへのアクセスが非常に容易かつ柔軟に行えるという利点を持つPHP 5.4をサポートする最新技術も採用されています。このほかにも、Cake PHPは、プログラマーのWebサイト開発を容易にするMVC技術も使用。セキュリティ面では、データの漏洩や盗難防止に非常に優れたセキュリティーシステムを有しています。

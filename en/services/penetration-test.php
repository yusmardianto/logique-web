                            controls.<br><br>
                            <div class="p-l-3">
                                <b>1) Web Apps</b><br>
                                A web application security test focuses only on evaluating the security of a web
                                application. The process involves an active analysis of the application for any
                                weaknesses, technical flaws, or vulnerabilities. Any security issues that are found will
                                be presented to the system owner, together with an assessment of the impact, a proposal
                                for mitigation or a technical solution.<br><br>
                                <b>2) Mobile Apps</b><br>
                                The security test on mobile apps focuses only on evaluating the security of a mobile app
                                on iOS or android devices. Like web apps, the process also involves an active analysis
                                of the application for any weaknesses, technical flaws, or vulnerabilities. <br><br>

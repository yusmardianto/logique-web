            <h3 class="section-title section-title--sub">Agile Development Methods</h3>
            <p class="mb-sm-30px">In developing the eBASTK application, the Agile development methods was used. Through this approach, iterative project management and software development methods (known as Sprint) are used through small and incremental stages whereby results are constantly evaluated and improved upon, allowing the developer to respond to changes quickly and efficiently.</p>
          </div>
          <div class="col-sm-6">
            <picture>
              <source srcset="new-img/icon-kotlin.webp" type="image/webp">

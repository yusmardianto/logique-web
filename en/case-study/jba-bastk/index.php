          <div class="col-md-offset-1 col-md-10">
            <p>Logique Digital Indonesia has succeeded in developing an efficient Auction Vehicle Handover application for JBA Bidwin Auction. The eBASTK application comes with a range of useful features, such as effectively handling and recording data during the handover process between sellers, brokers and dealers. In this regard, its functionality is highly useful, as important data which is sent to the JBA auctioning site will be highly accurate.</p>
          </div>
        </div>
      </section>

      <section class='cs-section client-info'>
        <h2 class="section-title">Background</h2>
        <p class="mb-30px">A universal problem experienced within the auction industry is the issue of valid data recording during the process of sending the vehicles to the auction location. This is because when goods are delivered, the items must be transferred through certain phases, each of which can be difficult to monitor, especially in regards to noting down the changes incurred by each of the vehicles as they pass through. Parts ending up lost are a common occurrence in this line of work, and making note of such events is difficult when it is done manually by pen and paper.</p>
        <p>The solution was fairly straightforward, through far from simple. JBA Indonesia, in collaboration with LOGIQUE, sought to digitize the process by developing an efficient recording and tracking application also capable of visualizing the circumstance taking place within each phase of the delivery process. This way, data about a vehicle’s overall condition is able to be taken into account systematically and in real time by each party involved.</p>
      </section>

      <section class='cs-section client-info'>
        <div class="row">
          <div class="col-md-7 text-container">
            <h2 class="section-title">eBASTK JBA application</h2>
            <p class="mb-30px">In partnering with LOGIQUE, JBA Indonesia was able to develop an application that systematically tracks and records vehicle conditions during the handover process. The eBASTK application does this in a thorough and user friendly manner, taking only 5 – 10 minutes to access the data in real time with a high degree of accuracy.</p>
            <p class="mb-30px">Also, the application take into account that most of these transactions are conducted in multiple locations, in areas such as vehicle pool locations, parking lots, reception areas, etc. Therefore, the eBASTK application functions to a high degree particularly as a mobile app.</p>
          </div>

              <p>Developing the eBASTK application, from the planning stage to the testing and maintenance phase was relitevly fast; specifically, between 4-5 months. The whole of the process was thoroughly planned and carried out in accordance to a predetermined schedule thanks to the high level of professionalism of each of LOGIQUE’s staff working on the project, as well as the excellent level of coordination with JBA Indonesia.</p>
            </div>

            <div class="mb-50px">
              <h2 class="section-title">Constraints & Challenges</h2>
              <p>There were no major setbacks during the development of the eBASTK application. However, the project cycle did require constant scrutiny and revision, as it was necessary to engage in good modeling practices in the requirement analysis phase and when conducting business process analyses. During this stage, efficient unit testing had to be conducted during development. Ultimately, any challenges faced made up valuable experiences for the LOGIQUE team which can be utilized to improve the quality of our services in the future.</p>

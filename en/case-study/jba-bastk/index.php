          <h2 class="section-title">Development & Technology Process</h2>
          <p>Development started with the planning and budgeting phases. After which, the business process was then analyzed. Then, various other processes, such as design, testing and maintenance were conducted.</p>
        </div>

        <div class="row">
          <div class="col-sm-6">

            <h3 class="section-title section-title--sub">Technology</h3>
            <p class="mb-sm-30px">Regarding the technology used to make the eBASTK system, the PHP platform was the top choice for the open source lineup, although MySQL, MariaDB or PostgreSQL databases are also compatible. Flutter was utilized in offering multi-platform options (Particularly iOS and Android).</p>
          </div>
        </div>
      </section>

      <section class="cs-section">
        <h2 class="section-title">The advantages of eBASTK</h2>
        <p class="mb-30px">The main advantage of this app is its checklist feature, which records the specific vehicle conditions during each phase of the mobilization process. Additionally, the notification feature lets the user know whenever there actually is an alteration in the status or condition of the vehicle throughout the transition process.</p>
        <p>Speed is also a prominent plus point of the app, as it can check the condition of a vehicle within 5 – 10 minutes. The speed at which the app conducts its assessment depends on the vehicles condition. Furthermore, the ‘real time’ functionality by which the app functions can serve as a huge advantage to users when data checking is required. This facilitates coordination greatly when there are revisions within the data that need to be made. Lastly, the data can be managed easily by both those who are involved with delivery and receiving the vehicles.</p>
      </section>


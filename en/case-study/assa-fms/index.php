              <p class="cs-subtitle-top">The Transportation Industry: Challenges</p>
              <p class="cs-desc custom4">
              As a leader in the provision of transportation services throughout Indonesia, ASSA maintains its focus in its ability to carry out effective vehicle management procedures. This is required so that companies can maintain or even improve levels of customer satisfaction, in the hope of the vehicle delivery process becoming a quicker and smoother endeavor.
              <br><br>
              This presents a general challenge for the transportation industry, especially for companies that are responsible for a large number of vehicles. This challenge lies in the fact that the manual vehicle management process is no longer possible. Managing vehicle lists through the use of spreadsheet documents by manually filling in vehicle data such as the type, color, driver, delivery date, and towing date will indeed take up a significant amount of time and further have a high chance of human error. This is coupled with the administrative handover process, which itself can be quite complicated, making the vehicle handover step a much more laborious process than it could be.
              <br><br>
              Another challenge that needs to be faced is in relation to the management of vehicle availability. This will present a problem if the company is unable to accurately track the number of vehicles that they own, both those within the pool as well as those that are being used outside. If this is allowed, the company will not be able to ensure that their vehicles will be available for use by the tenants.
              <br><br>
              Additionally, transportation companies, especially in regards to vehicle rental services, must also possess the ability to track the conditions and documents with detail when the vehicle is eventually returned by the consumer. This process is important, so that the company can immediately take the necessary actions if a problem has occurred with the vehicle. 
              </p>

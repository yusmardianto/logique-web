              <p class="cs-subtitle-top">Applied Technology</p>
              <p class="cs-desc custom4">
              In developing the ASSA Fleet Management System (FMS) web application, there were a variety of technologies used. Some of them, such as the Laravel framework, were chosen because of their inherent advantages, namely support for the MVC structure, scalability, good security, and a lighter performance. Lumen was also used as a micro framework for API development with the aim of providing high speeds and response times. Then, regarding the Front End, the Bootstrap Framework was used so that the website application would utilize an interactive, responsive appearance, and could work well on a variety of devices.
              <br><br>
              In developing this system, ASSA sought to digitize the vehicle handover process. Therefore, we also developed a mobile application in order to perform BASTK functions. This application was developed using Kotlin so it could run well on the Android platform.
              <br><br>
              Furthermore, at the database server layer, there was MySQL, namely the Relational Database Management System (RDBS) which was based on SQL (Structured Query Language). All of the above technologies were then combined with Alibaba Cloud, possessing reliable data processing capabilities. In addition, Alibaba Cloud has provided Anti-DDoS technology which serves to provide a better level of security.
              <br><br>  
              </p>

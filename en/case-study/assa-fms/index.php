              <p class="cs-subtitle-top">Fleet Management System (FMS)</p>
              <p class="cs-desc">
              The Fleet Management System (FMS) functions to properly manage and monitor leased vehicles. This system can perform a variety of tasks, from tracking the location of the vehicle, improving driver safety and security, to helping ensure the vehicle is always in top condition.
              <br><br>
              Broadly speaking, FMS will provide several solutions for transportation service providers, namely:
              <br><br>
                <ul class="ula">
                  <li>
                    <p><b>Supervising Vehicle Conditions</b></p>
                    <p class="normal-">With the help of FMS, companies can thoroughly monitor any vehicle in real-time. Companies can find out the available stock of vehicles within the pool, see if the vehicle is still on the way, or check if the customer has safely received the vehicle that they ordered.</p>
                    <br>
                  </li>
                  <li>
                    <p><b>Collecting Vehicle Data</b></p>
                    <p class="normal-" style="margin-bottom:10px;">Vehicles that are rented must be in condition in order to maintain the safety of both the driver and passenger. Therefore, companies need to monitor the condition of all their vehicles by collecting various data, such as:</p>
                    <p class="normal-"><span></span>Vehicle tracker and diagnostics</p>
                    <p class="normal- mb-2-">Real time data regarding the location and status of the vehicle can be used to track vehicles that are experiencing problems, so that the company can immediately send a replacement vehicle or send assistance to the driver.</p>
                    <p class="normal-"><span></span>Vehicle maintenance scheduling</p>
                    <p class="normal- mb-2-">The FMS may include maintenance alerts so as to ensure that the vehicle receives the proper services.</p>
                    <p class="normal-"><span></span>Fuel consumption tracker</p>
                    <p class="normal- mb-2-">FMS will also help track fuel usages so companies can better manage costs.</p>
                    <br>
                  </li>
                  <li>
                    <p><b>Supervising the Vehicle Delivery & Withdrawal Process</b></p>
                    <p class="normal-">Companies need to monitor the efficient process of sending and withdrawing vehicles. This is necessary to monitor the safety of the vehicle and the driver while on the way, manage driver service hours, as well as ensure the best service is provided for consumers.</p>
                    <br>
                    <p class="normal-">FMS can perform delivery management and vehicle withdrawals well. The process of sending vehicles to the tenant's location can be carried out in an organized manner because the system will make note of and inform the user of all the appropriate delivery details, starting from the vehicle type, the delivery date, the assigned driver, the delivery’s status, the consumer’s name, and other important articles of data.</p>
                    <br>
                    <p class="normal-">The same goes for the withdrawal management process. FMS will detail all the appropriate data related to the management of vehicle withdrawals from the customer's location. In addition, a comprehensive FMS also allows for companies to be able to carry out BASTK processes digitally.</p>
                  </li>
                </ul>
              <br><br><br>
              </p>

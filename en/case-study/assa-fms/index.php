              <p class="cs-subtitle-top">Background</p>
              <p class="cs-desc">
              Seeing the large number of vehicles and drivers that require management, ASSA has realized a need for an integrated management system to organize the fleets within each pool. The system must also include a comprehensive range of functions, such as being able to offer information regarding vehicle stocks, locations & conditions, service schedules for vendors, the operation of driver schedules, fuel consumption, as well as other issues. This is needed so that companies can better manage their business processes effectively, increasing customer satisfaction and maintaining durable vehicle performances. 
              <br><br>
              In order to realize this, ASSA has established a cooperative relationship with LOGIQUE DIGITAL INDONESIA with the development of the ASSA Fleet Management System (FMS) web application. This is supported by the BASTK mobile application and was designed to facilitate the process of renting vehicles owned by ASSA.
              <br><br>
              It is hoped that through this technology (appropriately named ASSA FMS), the company can manage thousands of leased fleet units in a more organized manner, from better and more efficient vehicle inspections, deliveries and recalls, to the effective maintenance & management of the vehicles themselves.

              <p class="cs-desc">
              As a leading insurance company, AIA aims to continuously improve its level of customer satisfaction; one way it seeks to do this is by changing its policy cancellation process from manual to digital. Currently, technology has grown increasingly advanced to the point that digital or online-based platforms have become the desirable option for most people. Realizing this fact, AIA decided to develop its current microsite project.
              <br><br>
              Through AIA Surrender’s microsite, the policy cancellation process can be done online. Policyholders simply should access the website wherever they are without having to contact the company. In addition, the microsite is also developed with retention tools. The purpose of this is to retain the customer from cancelling their insurance.

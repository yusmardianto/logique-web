						<div class="title">Penetration Testing?</div>
						<div class="row">
							<div class="col-md-offset-1 col-md-11">
								<p><br/>
								<b>Penetration testing</b> atau pentest merupakan istilah yang sering digunakan saat seseorang melakukan kegiatan pengujian sebuah sistem atau aplikasi. Kegiatan pengujian ini dilakukan untuk mengetahui apakah sistem keamanan yang terdapat pada sistem atau aplikasi mempunyai celah sehingga dapat segera diperbaiki dengan melakukan patch atau penambalan sehingga sistem keamanan yang terdapat pada suatu sistem atau aplikasi tersebut menjadi semakin kuat. Selain melakukan pengujian, kegiatan pentest juga melakukan dokumentasi sistem keamanan dari sistem atau aplikasi yang akan dilakukan pentest untuk selanjutnya dibuatkan laporan atau report kepada perusahaan/ pelanggan.<br/><br/>

								Sebelum melakukan pentest, biasanya akan terjadi kontrak antara auditor/ pentester dengan perusahaan yang ingin aplikasi atau sistemnya dilakukan pentest. Hal ini dalam konteks hukum merupakan kegiatan legal karena sebelumnya telah terjadi kontrak antara keduanya.

              <br><br>
              Merupakan tantangan industri transportasi secara umum, khususnya bagi perusahaan yang memiliki kendaraan dengan jumlah cukup banyak, bahwa proses manajemen kendaraan secara manual tidak lagi memungkinkan untuk dilakukan. Mengelola daftar kendaraan menggunakan dokumen spreadsheet dengan mengisi secara manual mengenai jenis, warna, tipe, driver, tanggal pengiriman, dan tanggal penarikan akan memakan banyak waktu dan memiliki peluang besar akan terjadinya human error. Ditambah dengan proses administrasi serah terima yang cukup rumit sehingga langkah penyerahan kendaraan bisa menjadi lebih panjang.

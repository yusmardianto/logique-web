              <p class="cs-desc custom4">
              Dalam mengembangkan aplikasi web ASSA Fleet Management System (FMS), ada banyak teknologi yang digunakan. Beberapa diantaranya seperti framework Laravel yang dipilih karena mempunyai keunggulan yaitu adanya dukungan struktur MVC, skalabilitas, keamanan yang baik, serta performa yang lebih ringan. Lumen juga digunakan sebagai framework mikro untuk pengembangan API dengan tujuan agar bisa memberikan kecepatan dan waktu respon yang tinggi. Kemudian pada sisi Front End-nya, Framework Bootstrap dipergunakan agar aplikasi website ini memiliki tampilan yang interaktif, responsif, dan dapat bekerja dengan baik di berbagai perangkat yang digunakan.

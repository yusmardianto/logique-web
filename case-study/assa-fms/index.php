              <p class="cs-subtitle-top">Fleet Management System (FMS)</p>
              <p class="cs-desc">
              Fleet Management System (FMS) adalah sebuah sistem yang berfungsi untuk mengelola dan memonitor kendaraan yang disewakan. Sistem ini dapat melakukan berbagai tugas mulai dari melacak lokasi kendaraan, meningkatkan keselamatan dan keamanan driver, sampai membantu memastikan kendaraan selalu dalam kondisi yang prima.
              <br><br>
              Secara garis besar, FMS akan memberikan beberapa solusi bagi perusahaan penyedia layanan transportasi yaitu:
              <br><br>
                <ul class="ula">
                  <li>
                    <p><b>Mengawasi Kondisi Kendaraan</b></p>
                    <p class="normal-">Dengan bantuan FMS, perusahaan dapat memonitor kendaraan apa pun secara real-time. Perusahaan dapat mengetahui stok kendaraan yang tersedia di pool, melihat apakah kendaraan masih di perjalanan, atau melihat apakah pelanggan telah menerima kendaraan yang mereka pesan dengan aman.</p>
                    <br>
                  </li>
                  <li>
                    <p><b>Mengumpulkan Data Kendaraan</b></p>
                    <p class="normal-" style="margin-bottom:10px;">Kendaraan yang disewakan harus memiliki kondisi yang baik untuk menjaga keselamatan driver ataupun penumpang. Oleh karena itu, perusahaan perlu memantau kondisi kendaraan dengan mengumpulkan berbagai data, seperti:</p>
                    <p class="normal-"><span></span>Pelacak dan diagnostik kendaraan</p>
                    <p class="normal- mb-2-">Data real time terkait lokasi dan status kendaraan dapat digunakan untuk melacak kendaraan yang mengalami masalah di perjalanan sehingga perusahaan dapat segera mengirim kendaraan pengganti atau bantuan untuk pengemudi.</p>
                    <p class="normal-"><span></span>Penjadwalan perawatan kendaraan</p>
                    <p class="normal- mb-2-">FMS dapat menyertakan peringatan perawatan untuk memastikan bahwa kendaraan menerima servis yang tepat.</p>
                    <p class="normal-"><span></span>Pelacak konsumsi bahan bakar</p>
                    <p class="normal- mb-2-">FMS juga akan membantu melacak penggunaan bahan bakar sehingga perusahaan dapat mengelola biaya dengan lebih baik.</p>
                    <br>
                  </li>
                  <li>
                    <p><b>Mengawasi Proses Pengiriman dan Penarikan Kendaraan</b></p>
                    <p class="normal-">Perusahaan perlu memantau proses pengiriman dan penarikan kendaraan dapat berjalan dengan lancar. Hal ini diperlukan untuk memonitor keselamatan kendaraan dan pengemudi di perjalanan, mengelola jam layanan pengemudi, serta memastikan layanan terbaik bagi konsumen.</p>
                    <br>
                    <p class="normal-">FMS dapat melakukan manajemen pengiriman dan penarikan kendaraan dengan baik. Proses pengiriman kendaraan ke lokasi penyewa dapat dilakukan secara terorganisir karena sistem akan menginformasikan detail pengiriman mulai dari jenis kendaraan, tanggal pengiriman, driver yang ditugaskan, status pengiriman, nama konsumen, dan lain-lain.</p>
                    <br>
                    <p class="normal-">Begitu pula dengan manajemen penarikan. FMS akan menginformasikan data terkait pengelolaan penarikan kendaraan dari lokasi konsumen. Selain itu, FMS yang komprehensif juga memungkinkan perusahaan untuk bisa melakukan proses BASTK secara digital.</p>

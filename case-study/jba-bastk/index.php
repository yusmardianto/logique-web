        <h2 class="section-title">Keunggulan eBASTK</h2>
        <p class="mb-30px">Fitur unggulan dari aplikasi ini adalah ceklis eBASTK. Fitur ini mampu mencatat dan merekam kondisi kendaraan di setiap titik transfer kendaraan selama proses mobilisasi. Selain itu terdapat pula fitur notifikasi perubahan status dan kondisi kendaraan, serta fitur-fitur lainnya berkaitan dengan proses pelaporan.</p>
        <p>Melalui Aplikasi eBASTK, proses pengecekan kondisi kendaraan menjadi jauh lebih efektif dan efisien. Hanya membutuhkan waktu 5 sampai 10 menit saja, tergantung kondisi kendaraan. Data kendaraan yang dihasilkan dapat dikirim kepada pihak-pihak yang membutuhkan secara real time, sesaat setelah pengecekan selesai dilakukan. Dan yang tidak kalah penting, masing-masing petugas dapat menandatangani hasil BASTK secara digital, baik untuk pihak yang menyerahkan maupun yang menerima kendaraan.</p>
      </section>


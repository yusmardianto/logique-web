      <section class='cs-section client-info'>
        <h2 class="section-title">Latar Belakang</h2>
        <p class="mb-30px">Salah satu permasalahan yang cukup besar dan kerap kali muncul dalam sebuah kegiatan lelang adalah mendapatkan data yang valid mengenai proses penelusuran rekam jejak kendaraan yang dikirimkan menuju ke balai lelang. Proses pengiriman yang terdiri dari beberapa rangkaian, terasa sangat menyulitkan untuk diawasi, khususnya jika terjadi perubahan pada kondisi kendaraan. Adanya permasalahan seperti kehilangan onderdil atau aksesoris pada kendaraan akan semakin sulit ditelusuri, terlebih jika masih dilakukan dengan proses administrasi konvensional dan manual menggunakan formulir kertas dalam pembuatan Berita Acara Serah Terima Kendaraan (BASTK).</p>
        <p class="mb-30px">JBA Indonesia sebagai salah satu perusahaan yang bergerak di bidang lelang otomotif dengan skala besar di Tanah Air pun pada awalnya tidak luput dengan kondisi serupa. </p>
        <p class="">Guna mendapatkan solusi untuk permasalahan tersebut, JBA Indonesia menggandeng LOGIQUE untuk megembangkan sebuah sistem yang mampu mencatat serta menyuguhkan visualisasi kendaraan pada tiap rangkaian proses pengiriman yang terjadi dengan baik dan sistematis. Sehingga, data mengenai kondisi kendaraan dapat ditelusuri dengan mudah secara real time dan dapat dipertanggung-jawabkan oleh masing-masing pihak.</p>
